void compute_derivative(){
    for (int i = 2; i < SAMPLE_LENGTH - 2; i++) {
        derivative[i] = (2*ecgBuffer[i+1] + ecgBuffer[i+2] - ecgBuffer[i-2] - 2*ecgBuffer[i-1]) / 8.0f;
    }
    // İlk ve son değerleri sıfırla
    derivative[0] = derivative[1] = 0.0f;
    derivative[SAMPLE_LENGTH-1] = derivative[SAMPLE_LENGTH-2] = 0.0f;
}

void compute_squared(){
    for(int i = 0; i < SAMPLE_LENGTH; i++) {
        squared[i] = derivative[i] * derivative[i];
    }
}

// moving window average
void compute_mwa(){
    // İlk MW_SIZE değerini sıfırla
    for(int i = 0; i < MW_SIZE; i++) {
        mwa[i] = 0.0f;
    }
    
    for(int i = MW_SIZE; i < SAMPLE_LENGTH; i++) {
        float sum = 0.0f;
        for(int j = 0; j < MW_SIZE; j++) {
            sum += squared[i-j];
        }
        mwa[i] = sum / MW_SIZE;
    }
}



void detect_peaks_improved() {
    peaks_counter = 0; // Counter'ı sıfırla
    
    // Adaptive threshold hesapla (maksimum değerin %40'ı)
    float max_mwa = 0.0f;
    for(int i = 0; i < SAMPLE_LENGTH; i++) {
        if(mwa[i] > max_mwa) {
            max_mwa = mwa[i];
        }
    }
    float threshold = max_mwa * 0.4f;
    
    // Minimum R-R mesafesi (128Hz örnekleme için ~0.5 saniye)
    int min_distance = 32; // 409 örnekte daha kısa mesafe kullan
    
    printf("Threshold: %.3f, Max MWA: %.3f\n", threshold, max_mwa);
    
    for(int i = min_distance; i < SAMPLE_LENGTH - min_distance; i++) {
        // Peak kontrolü: threshold üstünde ve lokal maksimum
        if(mwa[i] > threshold && 
           mwa[i] > mwa[i-1] && 
           mwa[i] > mwa[i+1]) {
            
            // Minimum mesafe kontrolü
            int too_close = 0;
            for(int j = 0; j < peaks_counter; j++) {
                if(abs(i - qrs_peaks[j]) < min_distance) {
                    too_close = 1;
                    break;
                }
            }
            
            // Peak'i kaydet
            if(!too_close && peaks_counter < MAX_PEAKS) {
                qrs_peaks[peaks_counter] = i;
                peaks_counter++;
            }
        }
    }
    
    printf("Bulunan peak sayisi: %d\n", peaks_counter);
    for (int i = 0; i < peaks_counter; i++) {
        printf("Peak %d: %d\n", i+1, qrs_peaks[i]);
    }
}

void calculate_bpm() {
    if (peaks_counter < 3) {
        printf("BPM hesaplamak icin yeterli peak bulunamadi (minimum 3 gerekli)\n");
        return;
    }
    
    float total_interval = 0.0f;
    int valid_intervals = 0;
    
    printf("\nR-R Intervalleri:\n");
    
    // Tüm R-R intervallerini hesapla
    for (int i = 1; i < peaks_counter; i++) {
        float interval = (float)(qrs_peaks[i] - qrs_peaks[i-1]) / 128.0f; // saniye cinsinden
        
        printf("Interval %d: %.3f s\n", i, interval);
        
        // Outlier kontrolü (0.3s - 2.0s arası fizyolojik olarak normal)
        if (interval > 0.3f && interval < 2.0f) {
            total_interval += interval;
            valid_intervals++;
        } else {
            printf("  -> Outlier, hesaplamaya katilmadi\n");
        }
    }
    
    if (valid_intervals > 0) {
        avg_rr_interval = total_interval / valid_intervals;
        bpm = 60.0f / avg_rr_interval;
        
        printf("\nOrtalama R-R interval: %.3f s\n", avg_rr_interval);
        printf("Hesaplanan BPM: %.1f\n", bpm);
        
        // BPM'in mantıklı aralıkta olup olmadığını kontrol et
        if (bpm < 40.0f || bpm > 200.0f) {
            printf("UYARI: BPM degeri normal aralikta degil!\n");
            healthStatus.bits.BPM_Err = 1;
        }else{
          healthStatus.bits.BPM_Err = 0;
        }
    } else {
        printf("Gecerli R-R interval bulunamadi\n");
    }
}

void calculate_qrs_interval(void){
    for(int i = 0; i < peaks_counter; i++){
        int r_index = qrs_peaks[i];

        int search_range = 15; // +-15 örneğe bakıyoruz
        int q_start = r_index - search_range;
        int s_end= r_index + search_range;

        if(q_start < 1 || s_end >= SAMPLE_LENGTH -1) continue;;
        
        int q_index = q_start;
        for(int j = q_start; j < r_index; j++){
            if(ecgBuffer[j] < ecgBuffer[q_index]){
                q_index = j;
            }
        }
        int s_index = r_index;
        for(int j = r_index + 1; j <= s_end; j++){
            if(ecgBuffer[j] < ecgBuffer[s_index]){
                s_index = j;
            }
        }
        qrs_duration = (float)(s_index - q_index) / 128.0f;
        if(qrs_duration > 0.12f){
          healthStatus.bits.QRS_Err = 1;
        }else{
          healthStatus.bits.QRS_Err = 0;
        }
    }
}

void calculate_p_t_points(void){
    for(int i = 0; i < peaks_counter; i++){
        int r_index = qrs_peaks[i];

        int p_start = r_index - (int)(0.25f * 128);
        int p_end   = r_index - (int)(0.10f * 128);
        if(p_start < 1 || p_end >= SAMPLE_LENGTH) {
            p_points[i] = -1; // geçersiz
        } else {
            int max_index = p_start;
            for(int j = p_start + 1; j <= p_end; j++){
                if(ecgBuffer[j] > ecgBuffer[max_index]){
                    max_index = j;
                }
            }
            p_points[i] = max_index;
        }

        int s_index = r_index;
        int s_search_end = r_index + (int)(0.1f * 128);
        if(s_search_end >= SAMPLE_LENGTH) s_search_end = SAMPLE_LENGTH - 1;
        for(int j = r_index + 1; j <= s_search_end; j++){
            if(ecgBuffer[j] < ecgBuffer[s_index]){
                s_index = j;
            }
        }

        int t_start = s_index + (int)(0.1f * 128);
        int t_end   = s_index + (int)(0.4f * 128);
        if(t_end >= SAMPLE_LENGTH) t_end = SAMPLE_LENGTH - 1;
        if(t_start >= SAMPLE_LENGTH) {
            t_points[i] = -1; // geçersiz
        } else {
            int max_index = t_start;
            for(int j = t_start + 1; j <= t_end; j++){
                if(ecgBuffer[j] > ecgBuffer[max_index]){
                    max_index = j;
                }
            }
            t_points[i] = max_index;
        }
    }
}

void calculate_pr_intervals(void){
    for(int i = 0; i < peaks_counter; i++){
        int r_index = qrs_peaks[i];
        int p_index = p_points[i];

        if(p_index == -1){
            continue;
        }
        pr_duration= (float) (r_index - p_index) / 128.0f;       
        if(pr_duration > 0.2f){
          healthStatus.bits.PRInt_Err = 1;
        }else{
          healthStatus.bits.PRInt_Err = 0;
        }
    }
}

void calculate_qt_intervals(void){
    for(int i = 0; i < peaks_counter; i++){
        int q_index = qrs_peaks[i];
        int t_index = t_points[i];
        
        if(t_index == -1) {
            continue;
        }
        qt_duration = (float)(t_index - q_index) / 128.0f;
        if(qt_duration > 0.44f){
          healthStatus.bits.QTInt_Err = 1;
        }else{
          healthStatus.bits.QTInt_Err = 0;
        }
    }
}
